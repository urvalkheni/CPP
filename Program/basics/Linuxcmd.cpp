#include <iostream>
using namespace std;

int main() {
    cout << "================ Linux Terminal Commands (C++) =================\n\n";
    cout << "1. clear      > Clears the screen.\n";
    cout << "2. date       > Shows current date and time.\n";
    cout << "3. uname -a   > Displays system info.\n";
    cout << "4. df -h      > Disk usage (human readable).\n";
    cout << "5. ls -l      > Lists directory in long format.\n";
    cout << "6. mkdir      > Creates a directory.\n";
    cout << "7. rmdir      > Removes empty directory.\n";
    cout << "8. touch      > Creates empty file.\n";
    cout << "9. cat        > Displays file contents.\n";
    cout << "10. cp        > Copies file.\n";
    cout << "11. mv        > Moves/renames file.\n";
    cout << "12. rm        > Deletes file.\n";
    cout << "13. chmod     > Changes file permissions.\n";
    cout << "14. chown     > Changes file owner.\n";
    cout << "15. whoami    > Displays current user.\n";
    cout << "16. top       > Shows running processes.\n";
    cout << "17. ps aux    > Lists all processes.\n";
    cout << "18. man       > Manual for commands.\n";
    cout << "19. history   > Lists command history.\n";
    cout << "20. exit      > Exits shell.\n";
    cout << "21. grep      > Searches for text in files.\n";
    cout << "22. echo      > Prints text to terminal.\n";
    cout << "23. alias     > Creates command shortcuts.\n";
    cout << "24. pwd       > Shows current working directory.\n";
    cout << "25. find      > Finds files by name.\n";
    return 0;
}
#include<iostream>
using namespace std; 

int main(){
    int num1,num2;
    cout<<"Enter The First Number :: ";
    cin>>num1;
    cout<<"Enter The Second Number :: ";
    cin>>num2;
    cout<<"Num1 + Num2 is :: "<<(num1+num2)<<endl;
    cout<<"Num1 - Num2 is :: "<<(num1-num2)<<endl;
    cout<<"Num1 / Num2 is :: "<<(num1/num2)<<endl;
    cout<<"Num1 * Num2 is :: "<<(num1*num2)<<endl;
    return 0;
}
